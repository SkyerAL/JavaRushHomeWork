/* 10 чисел
Вывести на экран числа от 1 до 10 используя цикл while.
*/

public class Solution
{
    public static void main(String[] args) throws Exception
    {
        int j=0;
        while (j<10){
            j++;
            System.out.println(j);
        }

    }
}